#include <stdlib.h>

#define TATE 8
#define YOKO 6

void codetoptn(char, char [TATE][YOKO]);
void strprint(char *);

int main( )
{
  char str[100];

  while(1){
    printf("Enter string => ");
    if(scanf("%s",str) == EOF) break;
    system("clear"); /* ←これで改ページを行う */
    strprint(str);
  }
}

void strprint(char *str){

  char *array[TATE],achar[TATE][YOKO];
  int i,j,k,n,count = 0;
  
  for(n = 0; str[n] != '\0'; n++);
  
  for(i = 0; i < TATE; i++) array[i] = (char*)malloc(YOKO*n);
  
  for(i = 0; i < n; i++){
    
    codetoptn(str[i],achar);
    
    for(k = 0;k < TATE; k++){

      for(j = 0 ; j < YOKO; j++){

	array[k][j+count] = achar[k][j];

      }
    }
    count += YOKO;
  }
  for(i = 0;i < TATE; i++){

    for(j = 0;j < YOKO; j++){

      printf("%c", array[i][j]);
    }
    printf("\n");
  }
}

